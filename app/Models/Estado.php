<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Estado extends Model
{
    protected $fillable = ['nombre'];

    /*
    |--------------------------------------------------------------------------
    | Clasificación inteligente por grupos
    |--------------------------------------------------------------------------
    */

    private ?string $grupoCache = null;

    private function grupo()
    {
        if ($this->grupoCache !== null) {
            return $this->grupoCache;
        }

        $this->grupoCache = match (trim($this->nombre)) {

            /*
            |--------------------------------------------------------------------------
            | VERDE
            | TERMINADO
            |--------------------------------------------------------------------------
            */

            'TERMINADO',
            'TERMINADO / LISTO PARA ENTREGA'
            => 'terminado',

            /*
            |--------------------------------------------------------------------------
            | AZUL CLARO
            | PENDIENTE POR AUTORIZAR
            |--------------------------------------------------------------------------
            */

            'PENDIENTE POR AUTORIZAR'
            => 'pendiente_autorizar',

            /*
            |--------------------------------------------------------------------------
            | AMARILLO / BEIGE
            | NO REPARADO / NO AUTORIZA
            |--------------------------------------------------------------------------
            */

            'EQ. NO REPARADO/NO AUTORIZA',
            'NO AUTORIZÓ REPARACIÓN'
            => 'no_reparado',

            /*
            |--------------------------------------------------------------------------
            | ROSA / BEIGE
            | EN ESPERA
            |--------------------------------------------------------------------------
            */

            'EN ESPERA'
            => 'en_espera',

            /*
            |--------------------------------------------------------------------------
            | AZUL MEDIO
            | EN ESPERA DE RECOLECCIÓN
            |--------------------------------------------------------------------------
            */

            'EN ESPERA DE RECOLECCIÓN'
            => 'espera_recoleccion',

            /*
            |--------------------------------------------------------------------------
            | AZUL OSCURO
            | PENDIENTE REFACCIÓN
            |--------------------------------------------------------------------------
            */

            'PENDIENTE REFACCIÓN'
            => 'pendiente_refaccion',

            /*
            |--------------------------------------------------------------------------
            | NARANJA QUEMADO
            | PENDIENTE COTIZACIÓN
            |--------------------------------------------------------------------------
            */

            'PENDIENTE COTIZACIÓN'
            => 'pendiente_cotizacion',

            /*
            |--------------------------------------------------------------------------
            | AMARILLO INTENSO
            | REPARADO PARA VENTA
            |--------------------------------------------------------------------------
            */

            'REPARADO PARA VENTA'
            => 'reparado_venta',

            /*
            |--------------------------------------------------------------------------
            | AZUL BRILLANTE
            | GARANTÍA / ASEGURADORA
            |--------------------------------------------------------------------------
            */

            'PROCESO GARANTÍA / ASEGURADORA',
            'PROCESO GARANTÍA / ASEGURADOR'
            => 'garantia',

            /*
            |--------------------------------------------------------------------------
            | GRIS
            | NO PRESENTÓ FALLA
            |--------------------------------------------------------------------------
            */

            'REVISADO / NO PRESENTÓ LA FALLA'
            => 'sin_falla',

            default
            => 'default',
        };

        return $this->grupoCache;
    }

    /*
    |--------------------------------------------------------------------------
    | Badge Color
    |--------------------------------------------------------------------------
    */

    public function getColorClaseAttribute()
    {
        return match ($this->grupo()) {

            /*
            |--------------------------------------------------------------------------
            | VERDE
            |--------------------------------------------------------------------------
            */

            'terminado'
            => 'bg-green-100 text-green-900',

            /*
            |--------------------------------------------------------------------------
            | AZUL CLARO
            |--------------------------------------------------------------------------
            */

            'pendiente_autorizar'
            => 'bg-sky-100 text-sky-900',

            /*
            |--------------------------------------------------------------------------
            | AMARILLO / BEIGE
            |--------------------------------------------------------------------------
            */

            'no_reparado'
            => 'bg-amber-100 text-amber-900',

            /*
            |--------------------------------------------------------------------------
            | ROSA / BEIGE
            |--------------------------------------------------------------------------
            */

            'en_espera'
            => 'bg-rose-300 text-rose-900',

            /*
            |--------------------------------------------------------------------------
            | AZUL MEDIO
            |--------------------------------------------------------------------------
            */

            'espera_recoleccion'
            => 'bg-blue-300 text-blue-950',

            /*
            |--------------------------------------------------------------------------
            | AZUL OSCURO
            |--------------------------------------------------------------------------
            */

            'pendiente_refaccion'
            => 'bg-blue-800 text-white',

            /*
            |--------------------------------------------------------------------------
            | NARANJA QUEMADO
            |--------------------------------------------------------------------------
            */

            'pendiente_cotizacion'
            => 'bg-orange-700 text-white',

            /*
            |--------------------------------------------------------------------------
            | AMARILLO INTENSO
            |--------------------------------------------------------------------------
            */

            'reparado_venta'
            => 'bg-yellow-300 text-yellow-950',

            /*
            |--------------------------------------------------------------------------
            | AZUL BRILLANTE
            |--------------------------------------------------------------------------
            */

            'garantia'
            => 'bg-cyan-500 text-white',

            /*
            |--------------------------------------------------------------------------
            | GRIS
            |--------------------------------------------------------------------------
            */

            'sin_falla'
            => 'bg-gray-300 text-gray-900',

            /*
            |--------------------------------------------------------------------------
            | DEFAULT
            |--------------------------------------------------------------------------
            */

            default
            => 'bg-gray-100 text-gray-800',
        };
    }

    /*
    |--------------------------------------------------------------------------
    | Border Superior
    |--------------------------------------------------------------------------
    */

    public function getBorderClaseAttribute()
    {
        return match ($this->grupo()) {

            'terminado'
            => 'border-t-4 border-green-200',

            'pendiente_autorizar'
            => 'border-t-4 border-sky-200',

            'no_reparado'
            => 'border-t-4 border-amber-400',

            'en_espera'
            => 'border-t-4 border-rose-500',

            'espera_recoleccion'
            => 'border-t-4 border-blue-400',

            'pendiente_refaccion'
            => 'border-t-4 border-blue-800',

            'pendiente_cotizacion'
            => 'border-t-4 border-orange-800',

            'reparado_venta'
            => 'border-t-4 border-yellow-400',

            'garantia'
            => 'border-t-4 border-cyan-600',

            'sin_falla'
            => 'border-t-4 border-gray-400',

            default
            => 'border-t-4 border-gray-300',
        };
    }
}
